function orderSave1(){
    ORDER = document.getElementById("whatorder");
    var nORDER = ORDER.options[ORDER.selectedIndex].value;
    //로컬에  order, 방문기록 저장
    localStorage.setItem("order", nORDER);
    window.location.href="1.html";
}

